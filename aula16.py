# if / elif / else

entrada = input('Você quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print('Você entrou no sistema')
elif entrada == 'sair':
    print('Você saiu do sistema ')
elif entrada == 'elif':
    print('Você digitou elif')
else:
    print('Você não digitou nem entrar nem sair.')
    
